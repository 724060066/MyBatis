package com.buba.pojo;


public class Study {

  private long id;
  private long classId;
  private long kId;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getClassId() {
    return classId;
  }

  public void setClassId(long classId) {
    this.classId = classId;
  }


  public long getKId() {
    return kId;
  }

  public void setKId(long kId) {
    this.kId = kId;
  }

}
